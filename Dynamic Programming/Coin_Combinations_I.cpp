#define output(a, b, n)                \
    for (long long i = b; i <= n; ++i) \
        cout << a[i] << " ";
#define Mod 1000000007
using namespace std;

void solve()
{
    int n, target;
    cin >> n >> target;
    vector<int>arr(n);
    for(int&x: arr)  
        cin >> x;
    vector<int>dp(target+1, 0);
    dp[0] = 1;
    for(int i = 1; i <= target; ++i){
        for(int j = 0; j < n; ++j){
            if(i >= arr[j])
                (dp[i] += dp[i-arr[j]]) %= Mod;
        }
    }
    cout << dp[target];
}
